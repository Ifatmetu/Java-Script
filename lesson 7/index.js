//Date object
var date=new Date();//for date
console.log(date);

var year=date.getFullYear();//for year
console.log(year);

var month=date.getMonth();//for Month
console.log(month);

var currentDate=date.getDate();//for Date
console.log(currentDate);

var currentDay=date.getDay();//for (saturday,sunday...)
console.log(currentDay);

var currentHour=date.getHours();//for hour
console.log(currentHour);

var currentMinutes=date.getMinutes();//for Minutes
console.log(currentMinutes);